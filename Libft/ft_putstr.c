#include "libft.h"

int	ft_putstr(char *s)
{
	int	x;

	x = 0;
	if (s == NULL)
		return (0);
	if (s[x] != '\0')
	{
		while (s[x] != '\0')
		{
			ft_putchar(s[x]);
			x++;
		}
	}
	return (x);
}
